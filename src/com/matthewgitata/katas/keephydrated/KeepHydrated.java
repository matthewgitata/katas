package com.matthewgitata.katas.keephydrated;

/**
 * The {@code  KeepHydrated} class contains a method to
 * find out number of litres of water drunk per coding
 * hour.
 *
 * created by @matthewgitata on 01/10/2023
 */

public class KeepHydrated  {
    public int Liters(double time)  {
        return (int) (time * 0.5);
    }
}
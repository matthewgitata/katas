package com.matthewgitata.katas.uniquenumber;

/**
 * The {@code UniqueNumber} class contains a method
 * that returns the number not equal to the rest.
 * <p>
 * created by @matthewgitata on 11/01/2023.
 */
public class UniqueNumber {
    /**
     * @param arr array of numbers
     * @return the unique number in {@code arr}
     */
    public static double findUniq(double arr[]) {
        // Do the magic
        return arr[0];
    }
}